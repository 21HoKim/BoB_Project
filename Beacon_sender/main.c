void usage()
{
    puts("Example");
    puts("./beacon_sender <interface>");
    puts("./beacon_sender wlan1");
}

bool parse(Param *param, int argc, char *argv[])
{
    if (argc != 2)
    {
        usage();
        return false;
    }
    param->dev_ = argv[1];
    printf("interface : %s\n", argv[1]);
    return true;
}

